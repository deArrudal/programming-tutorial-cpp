#include <iostream>
#include <cmath>

using namespace std;

int main()
{
    cout << 4 << endl;
    cout << -12 << endl; // negative numbers
    cout << 2.5 << endl;

    cout << 7 + 5 << endl;
    cout << 3 - 9 << endl;
    cout << 2 * 4 << endl;
    cout << 8 / 6 << endl;
    cout << 10 % 3 << endl; // modulo operator

    int wnum = 5;
    double dnum = 1.75;
    cout << wnum + dnum << endl;

    cout << pow(2, 4) << endl;
    cout << sqrt(169) << endl;
    cout << floor(8.3) << endl;
    cout << ceil(8.3) << endl;
    cout << fmax(3, 10) << endl;

    return 0;
}